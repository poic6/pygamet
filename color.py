def color(name):
    if name=='WHITE':
        return (255, 255, 255)
    elif name=='BLACK':
        return (0,0,0)
    elif name=='BLUE':
        return (20,60,120)
    elif name=='ORANGE':
        return (250, 170, 70)
    elif name=='RED':
        return (255, 0, 0)
    elif name=='GREEN':
        return (0, 255, 0)